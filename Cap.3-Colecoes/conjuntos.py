# CONJUNTOS (SETS)

# Um conjunto, diferente de uma lista, é uma coleção não ordenada e que não permite elementos duplicados

nomes = {'Goku', 'Vegeta', 'Trunks', 'Gohan', 'Trunks', 'Goku'}

print(nomes)

# Insert
nomes.add('Kami')

# Delete
nomes.remove('Vegeta')

